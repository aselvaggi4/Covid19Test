                </p>

                <ul>
                    <li>
                        <strong>Caso COVID-19 positivo&nbsp;<u>senza sintomi</u>:&nbsp;</strong>può rientrare in
                        comunità dopo un periodo di isolamento di almeno 10 giorni dalla comparsa della positività, al
                        termine del quale risulti eseguito un test molecolare con risultato negativo (10 giorni + test).
                    </li>
                    <li>
                        <strong>Caso COVID-19 positivo&nbsp;<u>con sintomi</u>:&nbsp;</strong>può rientrare in comunità
                        dopo un periodo di isolamento di almeno 10 giorni dalla comparsa dei sintomi (non considerando
                        anosmia e ageusia/disgeusia che possono avere prolungata persistenza nel tempo) accompagnato da
                        un test molecolare con riscontro negativo eseguito dopo almeno 3 giorni senza sintomi (10
                        giorni, di cui almeno 3 giorni senza sintomi + test).
                    </li>
                    <li>
                        <strong>Caso COVID-19 positivo&nbsp;<u>a lungo termine</u>:&nbsp;</strong>se&nbsp;continua a
                        risultare positivo al test molecolare, può interrompere l’isolamento dopo 21 giorni dalla
                        comparsa dei sintomi, in caso di assenza di sintomi da almeno una settimana (fatta eccezione per
                        la perdita di gusto e olfatto che possono durare per diverso tempo dopo la guarigione). È
                        tuttavia il medico a decidere sulla base delle condizioni del paziente,&nbsp;tenendo conto anche
                        dello stato immunitario delle persone interessate (nei pazienti immunodepressi il periodo di
                        contagiosità può essere più prolungato).
                    </li>
                </ul>

                <p>
                    La fine del periodo di isolamento/quarantena potrà essere certificata, a seconda dell’organizzazione
                    adottata dalle singole Regioni, dal Medico&nbsp;del&nbsp;Dipartimento di Prevenzione o Servizio di
                    Igiene Pubblica (SISP) della ASL competente per territorio o dal&nbsp;Medico di medicina generale
                    (MMG)/Pediatra di libera scelta (PLS).
                </p>

